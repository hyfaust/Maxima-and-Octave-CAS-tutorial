% 需要安装 Octave 的符号包（symbolic）
% =========================================================================

% 加载符号包

% 清除工作空间
clear;
clc;

fprintf('========================================\n');
fprintf('        变分法基础示例\n');
fprintf('========================================\n\n');

% -------------------------------------------------------------------------
% 1. 泛函的定义
% -------------------------------------------------------------------------
% 泛函是从函数空间到实数的一个映射
% 例如：J[y] = ∫_a^b F(x, y, y') dx
% 其中 F 是已知函数，y(x) 是未知函数，y' = dy/dx

% 示例泛函：J[y] = ∫_0^1 (y^2 + y'^2) dx
% 目标：寻找函数 y(x) 使得 J[y] 取极值

% 定义符号变量
syms x y(x);

% 定义被积函数 F(x, y, y')
F = y(x)^2 + diff(y(x), x)^2;

fprintf('1. 泛函定义\n');
fprintf("   考虑泛函：J[y] = ∫_0^1 (y^2 + y'^2) dx\n\n");

% -------------------------------------------------------------------------
% 2. 变分的计算
% -------------------------------------------------------------------------
% 变分 δJ 定义为：
% δJ = d/dε J[y + εη] |_{ε=0}
% 其中 η(x) 是满足边界条件的任意函数

% 计算泛函的一阶变分
% 对于 J[y] = ∫ F(x, y, y') dx
% δJ = ∫ (∂F/∂y * η + ∂F/∂y' * η') dx

fprintf('2. 变分计算\n');
fprintf('   计算泛函的一阶变分...\n\n');

% 计算 ∂F/∂y
F_y = diff(F, y(x));
fprintf('   ∂F/∂y = %s\n', char(F_y));

% 计算 ∂F/∂y'
F_yp = diff(F, diff(y(x), x));
fprintf('   ∂F/∂y'' = %s\n', char(F_yp));

% 对 ∂F/∂y' 项进行分部积分
% ∫ (∂F/∂y') η' dx = [∂F/∂y' * η]_a^b - ∫ d/dx(∂F/∂y') η dx
% 由于 η(a) = η(b) = 0，边界项消失

fprintf('\n   对 ∂F/∂y'' 项进行分部积分...\n');
fprintf('   ∫ (∂F/∂y'') η'' dx = - ∫ d/dx(∂F/∂y'') η dx\n');

% 计算 d/dx(∂F/∂y')
F_yp_diff = diff(F_yp, x);
fprintf('   d/dx(∂F/∂y'') = %s\n', char(F_yp_diff));

% 变分 δJ 的表达式
fprintf('\n   变分 δJ = ∫ (∂F/∂y - d/dx(∂F/∂y'')) η dx\n');
fprintf('   δJ = ∫ (%s - %s) η dx\n', char(F_y), char(F_yp_diff));

% -------------------------------------------------------------------------
% 3. 欧拉-拉格朗日方程
% -------------------------------------------------------------------------
% 要使 δJ = 0 对所有 η(x) 成立，必须满足：
% ∂F/∂y - d/dx(∂F/∂y') = 0
% 这就是欧拉-拉格朗日方程

fprintf('\n3. 欧拉-拉格朗日方程\n');
fprintf('   极值条件：∂F/∂y - d/dx(∂F/∂y'') = 0\n');

% 构造欧拉-拉格朗日方程
EL_equation = F_y - F_yp_diff == 0;
fprintf('   欧拉-拉格朗日方程：\n');
fprintf('   %s\n\n', char(EL_equation));

% 化简方程
EL_simplified = simplify(EL_equation);
fprintf('   化简后：\n');
fprintf('   %s\n\n', char(EL_simplified));

% -------------------------------------------------------------------------
% 4. 求解微分方程
% -------------------------------------------------------------------------
% 求解欧拉-拉格朗日方程，得到极值函数 y(x)

fprintf('4. 求解微分方程\n');
fprintf('   求解欧拉-拉格朗日方程...\n\n');

% 定义边界条件
% 假设 y(0) = 0, y(1) = 1
y0 = 0;
y1 = 1;

% 求解微分方程
% 注意：dsolve 可能无法求解所有方程，这里使用示例方程
% 对于 F = y^2 + y'^2，欧拉-拉格朗日方程是 y - y'' = 0
% 其通解为 y(x) = C1*e^x + C2*e^(-x)

% 手动求解简单情况
fprintf("   对于 F = y^2 + y'^2，欧拉-拉格朗日方程为：\n");
fprintf('   y - y'' = 0\n\n');

% 定义通解形式
syms C1 C2;
y_general = C1 * exp(x) + C2 * exp(-x);
fprintf('   通解为：y(x) = C1*e^x + C2*e^(-x)\n\n');

% 应用边界条件求解常数
% y(0) = 0 => C1 + C2 = 0 => C2 = -C1
% y(1) = 1 => C1*e + C2*e^(-1) = 1 => C1*(e - 1/e) = 1

% 求解 C1 和 C2
C1_val = 1 / (exp(1) - exp(-1));
C2_val = -C1_val;

% 极值函数
y_extremum = C1_val * exp(x) + C2_val * exp(-x);
y_extremum = simplify(y_extremum);

fprintf('   满足边界条件 y(0)=0, y(1)=1 的极值函数为：\n');
fprintf('   y(x) = %s\n\n', char(y_extremum));

% 化简为双曲函数形式
y_extremum_simplified = sinh(x) / sinh(1);
fprintf('   或等价地：\n');
fprintf('   y(x) = sinh(x) / sinh(1)\n\n');

% -------------------------------------------------------------------------
% 5. 数值验证
% -------------------------------------------------------------------------
% 使用数值方法验证解析结果

fprintf('5. 数值验证\n');
fprintf('   计算泛函值...\n\n');

% 定义数值函数
y_numeric = @(x) sinh(x) / sinh(1);

% 计算泛函 J[y] = ∫_0^1 (y^2 + y'^2) dx
% 首先计算被积函数
F_numeric = @(x) (sinh(x)/sinh(1)).^2 + (cosh(x)/sinh(1)).^2;

% 数值积分
J_value = quad(F_numeric, 0, 1);
fprintf("   泛函 J[y] = ∫_0^1 (y^2 + y'^2) dx = %.6f\n\n", J_value);

% 理论值：对于 y = sinh(x)/sinh(1)，J = coth(1)
J_theoretical = coth(1);
fprintf('   理论值：J = coth(1) = %.6f\n', J_theoretical);
fprintf('   误差：%.2e\n\n', abs(J_value - J_theoretical));

% -------------------------------------------------------------------------
% 6. 总结
% -------------------------------------------------------------------------
fprintf('========================================\n');
fprintf('            总结\n');
fprintf('========================================\n');
fprintf('1. 变分法研究泛函的极值问题\n');
fprintf('2. 泛函是函数空间到实数的映射\n');
fprintf('3. 极值条件导致欧拉-拉格朗日方程\n');
fprintf('4. 求解微分方程得到极值函数\n');
fprintf('5. 数值方法可以验证解析结果\n\n');

fprintf('示例完成。\n');